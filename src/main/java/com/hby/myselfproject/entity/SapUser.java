package com.hby.myselfproject.entity;

public class SapUser {

  private String id;
  private String name;
  private String id_no;
  private String mobile;
  private String personnel_area;
  private String office_name;
  private String full_office_name;
  private String position;
  private String syjs_date;
  private String hirdate;
  private String positive_date;
  private String status;
  private String login_name;

  public SapUser() {
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getId_no() {
    return id_no;
  }

  public void setId_no(String id_no) {
    this.id_no = id_no;
  }

  public String getMobile() {
    return mobile;
  }

  public void setMobile(String mobile) {
    this.mobile = mobile;
  }

  public String getPersonnel_area() {
    return personnel_area;
  }

  public void setPersonnel_area(String personnel_area) {
    this.personnel_area = personnel_area;
  }

  public String getOffice_name() {
    return office_name;
  }

  public void setOffice_name(String office_name) {
    this.office_name = office_name;
  }

  public String getFull_office_name() {
    return full_office_name;
  }

  public void setFull_office_name(String full_office_name) {
    this.full_office_name = full_office_name;
  }

  public String getPosition() {
    return position;
  }

  public void setPosition(String position) {
    this.position = position;
  }

  public String getSyjs_date() {
    return syjs_date;
  }

  public void setSyjs_date(String syjs_date) {
    this.syjs_date = syjs_date;
  }

  public String getHirdate() {
    return hirdate;
  }

  public void setHirdate(String hirdate) {
    this.hirdate = hirdate;
  }

  public String getPositive_date() {
    return positive_date;
  }

  public void setPositive_date(String positive_date) {
    this.positive_date = positive_date;
  }

  public String getStatus() {
    return status;
  }

  public void setStatus(String status) {
    this.status = status;
  }

  public String getLogin_name() {
    return login_name;
  }

  public void setLogin_name(String login_name) {
    this.login_name = login_name;
  }
}
